     *
     * @param  \App\Models\User  $user
     * @param  \App\Models\User  $model
     * @return \Illuminate\Auth\Access\Response|bool
     */
    public function view(User $user, User $model)
    {
        // Can't edit your own data from here..
        if($user->id == $model->id)
        {
            return Response::deny('You do not allowed to edit this user.');
        }

        // Admin can't edit superadmin
        if( $user->is_admin == 1)
        {
            return ( $model->is_superadmin == 1 ) ?
            Response::deny('You do not allowed to edit this user.'): Response::allow();
        } 

        return Response::allow();
    }

    
